        subroutine slepquad(c,eps,x,y,whts,npoint,n,w)
        implicit real *8 (a-h,o-z)
        save
        dimension x(1),y(1),whts(1),w(1)
c 
c       This subroutine constructs a Slepian quadrature on the disk.
c       For the user-specified size of the disk, it attempts to construct
c       a Slepian quadrature to the user-specified precision. Please
c       note that the subroutine does not actually construct the quadrature;
c       a collection of quadratures has been precomputed and stored; the
c       subroutine simply chooses the one it thinks fits best and returns
c       it to the user. The collection is fairly small, so the quadrature
c       chosen tends to fit a bigger disk than the user specified.
c       Furthermore, note that the largest disk for which the quadratures
c       are available has radius 24; for anything bigger than that, the
c       subroutine produces the quadrature designed for the disk of
c       radius 24. It is highly recommended that once the quadrature is
c       constructed, it be tested before use.
c 
c       The actual sizes of the disk for which the quadratures have been
c       stored are:
c 
c       1.0, 1.2, 2.8, 5.45, 6.0, 12.0, 24.0
c 
c 
c   ANOTHER IMPORTANT NOTE: The actual precision of the obtained quadrature
c             tends to be much higher than the precision specified. Often,
c             a proper calibration results in a drastically reduced number
c             of required nodes.
c 
c 
c       . . . allocate memory for the retrieval of the
c 
        len=1000
c 
        ixs=1
        iwhts=ixs+len
        iwork=iwhts+len
c 
c       find the value of the slepian parameter for which there are
c       quadratures available most closely approaching (from above) the
c       one requested by the user; retrieve them things from the
c       appropriate subroutine
c 
        if(c .gt. 1.15) goto 1100
c 
        call slep1p0(ier,eps,w(ixs),w(iwhts),n,rlam)
        goto 6000
 1100 continue
  
        if(c .gt. 1.25) goto 1200
c 
        call slep1p2(ier,eps,w(ixs),w(iwhts),n,rlam)
        goto 6000
 1200 continue
  
        if(c .gt. 2.81) goto 1300
c 
        call slep2p8(ier,eps,w(ixs),w(iwhts),n,rlam)
        goto 6000
 1300 continue
  
        if(c .gt. 5.46) goto 1400
c 
        call slep5p45(ier,eps,w(ixs),w(iwhts),n,rlam)
        goto 6000
 1400 continue
  
        if(c .gt. 6.25) goto 1500
c 
        call slep6p0(ier,eps,w(ixs),w(iwhts),n,rlam)
        goto 6000
 1500 continue
  
        if(c .gt. 12.1) goto 1700
c 
        call slep12p0(ier,eps,w(ixs),w(iwhts),n,rlam)
        goto 6000
 1700 continue
  
        call slep24p0(ier,eps,w(ixs),w(iwhts),n,rlam)
  
 6000 continue
c 
c        construct the quadrature on the disk
c 
        call prinf('in slepquad before slequadr, n=*',n,1)
        call prin2('in slepquad before slequadr, w(ixs)=*',w(ixs),n)
        call prin2(
     1      'in slepquad before slequadr, w(iwhts)=*',w(iwhts),n)
  
  
        call slequadr(c,w(ixs),w(iwhts),n,eps,
     1      x,y,whts,npoint,w(iwork) )
c 
        return
        end

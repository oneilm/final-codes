        subroutine slequadr(a,roots,whts,n,eps,
     1      x,y,whtsout,npoint,work)
        implicit real *8 (a-h,o-z)
        save
        dimension roots(1),whts(1),whtsout(1),x(1),y(1),
     1      work(1)
c 
c       construct all nodes for all values of r
c 
        npoint=0
        done=1
        pi=atan(done)*4
c 
        do 2000 i=1,n
c 
c        find the number of azimuthal nodes for this value of r
c 
        call slepjbf(a*roots(i),work(2),nfj)
c 
        do 1200 j=nfj,1,-1
        if(abs(work(j)) .gt. eps) goto 1400
        nazim=j
 1200 continue
 1400 continue
c 
        nazim=nazim*2
c 
c       construct the azimuthal nodes for this r
c 
c 
        h=2*pi/nazim
        r=roots(i)
c 
        do 1600 j=1,nazim
        theta=(j-1)*h
cccc        theta=(j-1)*h + h/4*(-1)**i
c 
        npoint=npoint+1
        x(npoint)=cos(theta)*r
        y(npoint)=sin(theta)*r
c 
        whtsout(npoint)=whts(i)*h
 1600 continue
c 
 2000 continue
  
        return
        end

        subroutine slepjbf(x,fjs,n)
        implicit real *8 (a-h,o-z)
        save
        dimension fjs(1)
c 
c       recurse up to find the highest order J-function
c       to be calculated
c 
        i0=0
        fjs(i0)=1
        fjs(1)=0
c 
        n=0
        rlarge=1.0d25
        nmax=100 000
        do 1200 i=1,nmax
c 
        n=i
        fjs(i+1)=(2*i/x)*fjs(i)-fjs(i-1)
        if(abs(fjs(i)) .gt. rlarge) goto 1400
 1200 continue
 1400 continue
c 
c       recurse down to find the unscaled J-functions
c 
        fjs(n)=0
        fjs(n-1)=1
        dd=0
        do 1600 i=n-1,1,-1
c 
        fjs(i-1)=(2*i/x)*fjs(i)-fjs(i+1)
c 
        dd=dd+fjs(i)**2
 1600 continue
c 
        dd=2*dd+fjs(i0)**2
c 
c       apply the final scaling
c 
        dd=1/sqrt(dd)
        do 1800 i=i0,n
c 
        fjs(i)=fjs(i)*dd
 1800 continue
        return
        end

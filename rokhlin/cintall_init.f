c 
        do 2400 i=1,m+1
        cints(i)=0
        sizes(i)=0
 2400 continue
c 
        do 2800 i=1,n
c 
        call cint_funeval(xs(i),rk,m,a,ndigits,fs)
c 
        do 2600 j=1,m+1
c 
        cints(j)=cints(j)+ws(i)*fs(j)
        sizes(j)=sizes(j)+ws(i)*abs(fs(j))
 2600 continue
c 
 2800 continue
c 
        return
        end
  

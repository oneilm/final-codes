c 
c       determine the size of the ball in which the
c       quadratures will be designed
c 
        eps=0.1**ndigits
c 
        rkrea=rk
        rkima=-ima*rk
c 
        rmax1=40/rkima
        rmax2=1.0d30
        if(rkrea .lt. 0) rmax2=log(eps)/rkrea
c 
cccc        call prin2('rmax1=*',rmax1,1)
cccc        call prin2('rmax2=*',rmax2,1)

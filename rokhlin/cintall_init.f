c  n - the number of Gaussian nodes to be used in the evaluation
c        of the integral. Recommended value: 50+
c 
c                  Output parameters:
c 
c  a - the length of the interval of integration in (1)
c  cints - the intergals (1); m+1 of them things
c  sizes - the integrals on the interval [0,a] of the absolute
c        values of the integrand in (1)
c 
c                  Work arrays:
c 
c  w - must be at least 2*(n+m)+100 real *8 elements long
c 
c 
        ixs=1
        lxs=n+2
c 
        iws=ixs+lxs
        lws=n+2
c 
        ifs=iws+lws
        lfs=m*2+20
c 
        call cints_eval0(rk,m,ndigits,n,
     1      cints,w(ixs),w(iws),sizes,w(ifs),a)
c 
        return
        end

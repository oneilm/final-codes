        rmax=rmax1
        if(rmax2 .lt. rmax) rmax=rmax2
c 
        call prin2('and rmax=*',rmax,1)
c 
c        discretize the obtained interval
c 
        a=rmax
        itype=21
        call legeexps(itype,n,xs,u,v,ws)
  
        do 2200 i=1,n
c 
        xs(i)=(xs(i)+1)*a/2
        ws(i)=ws(i)*a/2
 2200 continue

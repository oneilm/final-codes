        subroutine cints_eval(rk,m,ndigits,n,
     1      a,cints,sizes,w)
        implicit real *8 (a-h,o-z)
        save
        dimension sizes(1),w(1)
c 
        complex *16 rk,cints(1)
c 
c        This subroutine evaluates the integrals
c 
c        \int_0^a bell(x/a) * e^{rk*x} x^j dx                   (1)
c 
c        on the interval [0,a]; in (1) above, bell is the
c        prolate bell returned by the subroutine protaper
c        (see), j=0,1,2,...,m with user-specified m, and
c        a is determined by this subroutine in such a manner
c        that the interval [0,a] contains about six wavelengths

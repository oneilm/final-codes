        subroutine cintgrid(n,a,xyzs,h)
        implicit real *8 (a-h,o-z)
        save
        dimension xyzs(3,n,n,n)
c 
c        discretize the 3-dimensional cube
c 
        h=2*a/(n-1)
        do 2000 i=1,n
        do 1800 j=1,n
        do 1600 k=1,n
